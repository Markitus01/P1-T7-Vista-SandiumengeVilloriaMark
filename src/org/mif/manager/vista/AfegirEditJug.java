    public AfegirEditJug(Jugador jug) {
        initComponents();
        
        sexeGrup.add(homeRadioButton);
        sexeGrup.add(donaRadioButton);
        
        this.jugadorEditant = jug;
        
        if (jug != null)
        {
             // Editar: omple camps
            nomTextField.setText(jug.getNom());
            cognomsTextField.setText(jug.getCognoms());
            nifTextField.setText(jug.getIdLegal());
            // Data naixement en format dd/MM/yyyy
            if (jug.getData_naix() != null)
            {
                java.time.format.DateTimeFormatter formatter = java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy");
                naixTextField.setText(jug.getData_naix().format(formatter));
            }
            else
            {
                naixTextField.setText("");
            }
            // Sexe (assumint "H" o "D")
            if (jug.getSexe() != null)
            {
                if (jug.getSexe().equalsIgnoreCase("H"))
                {
                    homeRadioButton.setSelected(true);
                }
                else if (jug.getSexe().equalsIgnoreCase("D"))
                {
                    donaRadioButton.setSelected(true);
                }
            }
            else
            {
                // Cap seleccionat si null
                sexeGrup.clearSelection();
            }

            if (jug.getIban() != null)
            {
                ibanTextField.setText(jug.getIban());
            }
            else
            {
                ibanTextField.setText("");
            }

            if (jug.getAny_fi_revi_medica().getYear() > 0)
            {
                reviMedicaTextField.setText(String.valueOf(jug.getAny_fi_revi_medica().getYear()));
            }
            else
            { 
                reviMedicaTextField.setText("");
            }
            
            if (jug.getAdresa() != null)
            {
                adrecaTextField.setText(jug.getAdresa());
            }
            else
            {
                adrecaTextField.setText("");
            }
            
            naixTextField.setEditable(false);
            homeRadioButton.setEnabled(false);
            donaRadioButton.setEnabled(false);
        }
    }

