    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed
        // Buidem errors
        errNomLabel.setText("");
        errCogLabel.setText("");
        errSexLabel.setText("");
        errNaixLabel.setText("");
        errNifLabel.setText("");
        errIbanLabel.setText("");
        errAdrLabel.setText("");
        errReviMedLabel.setText("");

        boolean hasError = false;

        String nom = nomTextField.getText().trim();
        String cognoms = cognomsTextField.getText().trim();
        String nif = nifTextField.getText().trim();
        String dataNaixStr = naixTextField.getText().trim();
        
        String sexe = null;
        if (homeRadioButton.isSelected())
        { 
            sexe = "H"; 
        }
        else if (donaRadioButton.isSelected()) 
        { 
            sexe = "D"; 
        }
        
        String iban = ibanTextField.getText().trim();
        String adreca = adrecaTextField.getText().trim();
        String anyFiReviMedStr = reviMedicaTextField.getText().trim();

        // VALIDACIÓ: només es poden modificar sexe/dataNaix si és un nou jugador!
        if (jugadorEditant != null) {
            dataNaixStr = ""; // No permetre editar
            sexe = null;
        }

        // Validem els camps de manera bàsica
        if (nom.isEmpty())
        { 
            errNomLabel.setText("Nom obligatori."); 
            hasError = true; 
        }
        if (cognoms.isEmpty())
        { 
            errCogLabel.setText("Cognoms obligatoris."); 
            hasError = true; 
        }
        if (nif.isEmpty())
        { 
            errNifLabel.setText("NIF obligatori."); 
            hasError = true; 
        }
        // Només demanem data naixement i sexe si és un nou jugador
        if (jugadorEditant == null)
        {
            if (sexe == null)
            { 
                errSexLabel.setText("Selecciona sexe."); 
                hasError = true; 
            }
            if (dataNaixStr.isEmpty())
            { 
                errNaixLabel.setText("La data de naixement és obligatòria."); 
                hasError = true; 
            }
        }
        if (iban.isEmpty())
        { 
            errIbanLabel.setText("IBAN obligatori."); 
            hasError = true; 
        }
        if (adreca.isEmpty())
        { 
            errAdrLabel.setText("Adreça obligatòria."); 
            hasError = true; 
        }

        // Validació d'IBAN
        String ibanClean = iban.replaceAll("\\s", "");
        if (!ibanClean.matches("^ES\\d{22}$"))
        {
            errIbanLabel.setText("Format incorrecte.");
            hasError = true;
        }

        // Validació d’adreça (visual)
        if (!adreca.isEmpty() && adreca.chars().filter(ch -> ch == ',').count() < 2)
        {
            errAdrLabel.setText("Format incorrecte");
            hasError = true;
        }

        // Validació de “Any fi revisió” (visual)
        java.time.LocalDate anyFiReviMedica = null;
        if (!anyFiReviMedStr.isEmpty())
        {
            if (!anyFiReviMedStr.matches("^\\d{4}$"))
            {
                errReviMedLabel.setText("Han de ser 4 xifres.");
                hasError = true;
            }
            else
            {
                int any = Integer.parseInt(anyFiReviMedStr);
                int anyActual = java.time.LocalDate.now().getYear();
                if (any <= anyActual)
                {
                    errReviMedLabel.setText("Ha de ser un any futur.");
                    hasError = true;
                } 
                else 
                {
                    anyFiReviMedica = java.time.LocalDate.of(any, 1, 1);
                }
            }
        }

        // Validació de data naixement (només si és nou)
        java.time.LocalDate dataNaix = null;
        if (jugadorEditant == null && !dataNaixStr.isEmpty()) 
        {
            try 
            {
                java.time.format.DateTimeFormatter formatter = java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy");
                dataNaix = java.time.LocalDate.parse(dataNaixStr, formatter);
            } 
            catch (Exception ex) 
            {
                errNaixLabel.setText("Format ha de ser dd/mm/yyyy");
                hasError = true;
            }
        }

        if (hasError) { return; }

        try
        {
            Jugador jug;
            if (jugadorEditant == null) { jug = new Jugador(); }
            else { jug = jugadorEditant; }

            jug.setNom(nom);
            jug.setCognoms(cognoms);
            jug.setIdLegal(nif);
            jug.setIban(ibanClean);
            jug.setAdresa(adreca);
            if (anyFiReviMedica != null) 
            { 
                jug.setAny_fi_revi_medica(anyFiReviMedica); 
            }
            else 
            {
                jug.setAny_fi_revi_medica(null); 
            }
            // Només per crear nou jugador
            if (jugadorEditant == null) 
            {
                jug.setSexe(sexe);
                jug.setData_naix(dataNaix);
            }

            boolean esNou = (jugadorEditant == null);
            String error = Utils.guardarJugador(jug, esNou);
            if (error != null) 
            {
                javax.swing.JOptionPane.showMessageDialog(this, "No s'ha pogut desar el jugador: " + error, "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            GestJugsFrame frame = new GestJugsFrame();
            frame.setVisible(true);
            this.dispose();
        }
        catch (RuntimeException ex) 
        {
            // Si l'error es diferent dels que controlem aqui, comprovem els missatges que retorna el model de jugador
            String msg = ex.getMessage();
            if (msg.contains("Nom")) { errNomLabel.setText(msg); }
            else if (msg.contains("Cognoms")) { errCogLabel.setText(msg); }
            else if (msg.contains("Sexe")) { errSexLabel.setText(msg); }
            else if (msg.contains("no futura")) { errNaixLabel.setText(msg); }
            else if (msg.contains("Format")) { errNifLabel.setText(msg); }
            else if (msg.contains("IBAN")) { errIbanLabel.setText("Format incorrecte."); }
            else if (msg.contains("adreça")) { errAdrLabel.setText("Format incorrecte."); }
            else if (msg.contains("Data")) { errReviMedLabel.setText("Any futur obligatori"); }
            else { javax.swing.JOptionPane.showMessageDialog(this, msg, "Error", javax.swing.JOptionPane.ERROR_MESSAGE); }
        }
    }//GEN-LAST:event_guardarButtonActionPerformed

