    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String login = userField.getText();
        String password = new String(pswdField.getPassword());
        
        try 
        {
           if (Utils.getGBD().loginUsuari(login, password) == 1)
           {
               SelTempFrame tempsFrame = new SelTempFrame();
               tempsFrame.setVisible(true);
               
               this.dispose();
           }
           else
           {
               infoLabel.setText("Credencials incorrectes!");
           }
        }
        catch (GestorBDManagerException ex)
        {
            
            infoLabel.setText("Error: "+ex.getMessage());
        }
    }//GEN-LAST:event_loginButtonActionPerformed

